            CHECK (moderation_status IN ('PENDING', 'APPROVED', 'REJECTED', 'TAKEN_DOWN'));
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_playlists_moderation_status') THEN
        ALTER TABLE playlists
            ADD CONSTRAINT ck_playlists_moderation_status
            CHECK (moderation_status IN ('PENDING', 'APPROVED', 'REJECTED', 'TAKEN_DOWN'));
    END IF;
