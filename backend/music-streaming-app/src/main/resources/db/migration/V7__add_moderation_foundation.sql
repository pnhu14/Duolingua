ALTER TABLE users ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMPTZ;

ALTER TABLE users DROP CONSTRAINT IF EXISTS ck_users_status;
ALTER TABLE users
    ADD CONSTRAINT ck_users_status
    CHECK (status IN ('ACTIVE', 'DISABLED', 'PENDING', 'SUSPENDED', 'DELETED'));

ALTER TABLE songs ADD COLUMN IF NOT EXISTS submitted_by_user_id UUID;
ALTER TABLE songs ADD COLUMN IF NOT EXISTS moderation_status VARCHAR(32) NOT NULL DEFAULT 'APPROVED';
ALTER TABLE songs ADD COLUMN IF NOT EXISTS published_at TIMESTAMPTZ;
ALTER TABLE songs ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMPTZ;

ALTER TABLE albums ADD COLUMN IF NOT EXISTS submitted_by_user_id UUID;
ALTER TABLE albums ADD COLUMN IF NOT EXISTS moderation_status VARCHAR(32) NOT NULL DEFAULT 'APPROVED';
ALTER TABLE albums ADD COLUMN IF NOT EXISTS published_at TIMESTAMPTZ;
ALTER TABLE albums ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMPTZ;

ALTER TABLE artists ADD COLUMN IF NOT EXISTS moderation_status VARCHAR(32) NOT NULL DEFAULT 'APPROVED';
ALTER TABLE artists ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMPTZ;

ALTER TABLE playlists ADD COLUMN IF NOT EXISTS moderation_status VARCHAR(32) NOT NULL DEFAULT 'APPROVED';
ALTER TABLE playlists ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMPTZ;

ALTER TABLE songs DROP CONSTRAINT IF EXISTS ck_songs_status;
ALTER TABLE songs
    ADD CONSTRAINT ck_songs_status
    CHECK (status IN ('DRAFT', 'PUBLISHED', 'ARCHIVED', 'TAKEN_DOWN', 'DELETED'));

ALTER TABLE albums DROP CONSTRAINT IF EXISTS ck_albums_status;
ALTER TABLE albums
    ADD CONSTRAINT ck_albums_status
    CHECK (status IN ('DRAFT', 'PUBLISHED', 'ARCHIVED', 'TAKEN_DOWN', 'DELETED'));

ALTER TABLE artists DROP CONSTRAINT IF EXISTS ck_artists_status;
ALTER TABLE artists
    ADD CONSTRAINT ck_artists_status
    CHECK (status IN ('ACTIVE', 'INACTIVE', 'ARCHIVED', 'SUSPENDED', 'DELETED'));

ALTER TABLE playlists DROP CONSTRAINT IF EXISTS ck_playlists_status;
ALTER TABLE playlists
    ADD CONSTRAINT ck_playlists_status
    CHECK (status IN ('ACTIVE', 'ARCHIVED', 'TAKEN_DOWN', 'DELETED'));

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_songs_submitted_by_user') THEN
        ALTER TABLE songs
            ADD CONSTRAINT fk_songs_submitted_by_user
            FOREIGN KEY (submitted_by_user_id)
            REFERENCES users (user_id)
            ON DELETE SET NULL;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_albums_submitted_by_user') THEN
        ALTER TABLE albums
            ADD CONSTRAINT fk_albums_submitted_by_user
            FOREIGN KEY (submitted_by_user_id)
            REFERENCES users (user_id)
            ON DELETE SET NULL;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_songs_moderation_status') THEN
        ALTER TABLE songs
            ADD CONSTRAINT ck_songs_moderation_status
            CHECK (moderation_status IN ('PENDING', 'APPROVED', 'REJECTED', 'TAKEN_DOWN'));
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_albums_moderation_status') THEN
        ALTER TABLE albums
            ADD CONSTRAINT ck_albums_moderation_status
            CHECK (moderation_status IN ('PENDING', 'APPROVED', 'REJECTED', 'TAKEN_DOWN'));
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'ck_artists_moderation_status') THEN
