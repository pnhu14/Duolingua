CREATE TABLE IF NOT EXISTS reports (
    report_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reporter_user_id UUID NOT NULL,
    entity_type VARCHAR(32) NOT NULL,
    entity_id VARCHAR(80) NOT NULL,
    reason VARCHAR(64) NOT NULL,
    description TEXT,
    status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    reviewed_by_user_id UUID,
    reviewed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_reports_reporter_user
        FOREIGN KEY (reporter_user_id)
        REFERENCES users (user_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_reports_reviewed_by_user
        FOREIGN KEY (reviewed_by_user_id)
        REFERENCES users (user_id)
        ON DELETE SET NULL,
    CONSTRAINT ck_reports_entity_type CHECK (entity_type IN ('USER', 'ARTIST', 'ALBUM', 'SONG', 'PLAYLIST', 'COMMENT')),
    CONSTRAINT ck_reports_reason CHECK (reason IN ('SPAM', 'HARASSMENT', 'COPYRIGHT', 'EXPLICIT', 'MISINFORMATION', 'OTHER')),
    CONSTRAINT ck_reports_status CHECK (status IN ('PENDING', 'REVIEWING', 'DISMISSED', 'ACTIONED'))
);

CREATE TABLE IF NOT EXISTS moderation_actions (
    moderation_action_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    moderator_user_id UUID,
    target_type VARCHAR(32) NOT NULL,
    target_id VARCHAR(80) NOT NULL,
    action VARCHAR(64) NOT NULL,
    reason TEXT,
    metadata JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_moderation_actions_moderator_user
        FOREIGN KEY (moderator_user_id)
        REFERENCES users (user_id)
        ON DELETE SET NULL,
    CONSTRAINT ck_moderation_actions_target_type CHECK (target_type IN ('USER', 'ARTIST', 'ALBUM', 'SONG', 'PLAYLIST', 'COMMENT')),
    CONSTRAINT ck_moderation_actions_action CHECK (action IN ('WARN', 'APPROVE', 'REJECT', 'HIDE', 'RESTORE', 'TAKE_DOWN', 'SUSPEND_USER', 'DELETE'))
);

CREATE INDEX IF NOT EXISTS idx_users_status_suspended_until ON users (status, suspended_until);
CREATE INDEX IF NOT EXISTS idx_users_deleted_at ON users (deleted_at);
CREATE INDEX IF NOT EXISTS idx_songs_moderation_status ON songs (moderation_status);
CREATE INDEX IF NOT EXISTS idx_songs_submitted_by_user_id ON songs (submitted_by_user_id);
CREATE INDEX IF NOT EXISTS idx_songs_deleted_at ON songs (deleted_at);
CREATE INDEX IF NOT EXISTS idx_albums_moderation_status ON albums (moderation_status);
CREATE INDEX IF NOT EXISTS idx_artists_moderation_status ON artists (moderation_status);
CREATE INDEX IF NOT EXISTS idx_playlists_moderation_status ON playlists (moderation_status);
CREATE INDEX IF NOT EXISTS idx_reports_reporter_user_id ON reports (reporter_user_id);
CREATE INDEX IF NOT EXISTS idx_reports_entity ON reports (entity_type, entity_id);
CREATE INDEX IF NOT EXISTS idx_reports_status_created_at ON reports (status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_moderation_actions_target ON moderation_actions (target_type, target_id);
CREATE INDEX IF NOT EXISTS idx_moderation_actions_moderator_user_id ON moderation_actions (moderator_user_id);
CREATE INDEX IF NOT EXISTS idx_moderation_actions_created_at ON moderation_actions (created_at DESC);

COMMIT;
